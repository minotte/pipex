#include "ft_test.h"

int	ft_strcmp_str(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2 || (ft_strlen(s1) != ft_strlen(s2)))
		return (0);
	while ((s1[i] == s2[i]) && (s1[i] != '\0'))
		i++;
	if (s1[i] == '\0' && s2[i] == '\0')
		return (1);
	return (0);
}
